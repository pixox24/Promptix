export function getTemplateById(id: string): PromptTemplate | undefined {
  return templates.find((t) => t.id === id);
}

export function getFeaturedTemplates(): PromptTemplate[] {
  return templates.filter((t) => t.isFeatured);
}

export function getHotTemplates(limit = 8): PromptTemplate[] {
  return [...templates]
    .sort((a, b) => b.useCount - a.useCount)
    .slice(0, limit);
}

export function getLatestTemplates(limit = 8): PromptTemplate[] {
  return [...templates]
    .sort(
      (a, b) =>
        new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime(),
    )
    .slice(0, limit);
}

export function getSimilarTemplates(
  template: PromptTemplate,
  limit = 4,
): PromptTemplate[] {
  return templates
    .filter((t) => t.id !== template.id)
    .map((t) => {
      let score = 0;
      if (t.category === template.category) score += 3;
      score += t.tags.filter((tag) => template.tags.includes(tag)).length;
      return { t, score };
    })
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(({ t }) => t);
}
